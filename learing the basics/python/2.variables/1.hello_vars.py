#strings are simply Variables and are assigned with quotes. Treat variables as a form of Label in python that holds a value
stringMessage = "Hello World!"

#when using a string variable, you simply need to use it as is, no quotes needed
print(stringMessage)

#changing the message printed to screen
stringMessage = "I changed!"

print(stringMessage)

#adding another string to an existing message
stringMessage = stringMessage + ", Hello again!"

print(stringMessage)

#writting a string with single quotes is explicit. a great way to add double quotes
stringMessage = 'this is "really" neat!'

print(stringMessage)